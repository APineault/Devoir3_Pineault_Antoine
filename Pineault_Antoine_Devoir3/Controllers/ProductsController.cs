using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using Pineault_Antoine_Devoir3;
using System.IO;

namespace Pineault_Antoine_Devoir3.Controllers
{
    public class ProductsController : Controller
    {
        string imgpath;
        private NorthwndEntities db = new NorthwndEntities();
